package com.daily.dto;

public class BankAccount {
	private int acct_code;
	private String acct_name;
	private String acct_bank;
	private String acct_owner;
	private String acct_id;
	private String acct_cla;
	private String acct_memo;
	private String admin_key;
	public int getAcct_code() {
		return acct_code;
	}
	public void setAcct_code(int acct_code) {
		this.acct_code = acct_code;
	}
	public String getAcct_name() {
		return acct_name;
	}
	public void setAcct_name(String acct_name) {
		this.acct_name = acct_name;
	}
	public String getAcct_bank() {
		return acct_bank;
	}
	public void setAcct_bank(String acct_bank) {
		this.acct_bank = acct_bank;
	}
	public String getAcct_owner() {
		return acct_owner;
	}
	public void setAcct_owner(String acct_owner) {
		this.acct_owner = acct_owner;
	}
	public String getAcct_id() {
		return acct_id;
	}
	public void setAcct_id(String acct_id) {
		this.acct_id = acct_id;
	}
	public String getAcct_cla() {
		return acct_cla;
	}
	public void setAcct_cla(String acct_cla) {
		this.acct_cla = acct_cla;
	}
	public String getAcct_memo() {
		return acct_memo;
	}
	public void setAcct_memo(String acct_memo) {
		this.acct_memo = acct_memo;
	}
	public String getAdmin_key() {
		return admin_key;
	}
	public void setAdmin_key(String admin_key) {
		this.admin_key = admin_key;
	}
	
	
}
